# ╔═╡ e79c762e-a609-11ec-12da-bb95264acadb
function calculate_anomaly_vector_category(point_communities, window_size)
	n = length(point_communities)
	anomalies = zeros(n)

	for i in 1:(n-window_size)
		elems_in_window = point_communities[i:(i + window_size)]
		baseline = mode(elems_in_window)
		for j in 1:window_size
			if elems_in_window[j] != baseline
				anomalies[i + j - 1] += 1
			end
		end
	end

	anomalies
end

