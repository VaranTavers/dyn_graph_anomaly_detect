# ╔═╡ 8e05dd24-c9cc-4b7a-a429-b601d3af5514
function calculate_anomaly_vector_quantity(point_communities, window_size, dif_rate)
	n = length(point_communities)
	anomalies = zeros(n)

	for i in 1:(n-window_size)
		elems_in_window = point_communities[i:(i + window_size)]
		baseline = mean(elems_in_window)
		for j in 1:window_size
			if abs(elems_in_window[j] - baseline) > baseline * dif_rate
				anomalies[i + j - 1] += 1
			end
		end
	end

	anomalies
end

